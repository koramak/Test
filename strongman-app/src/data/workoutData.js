export const getExerciseById = (dayId, exerciseId) => {
  const day = workoutDays.find(d => d.id === dayId);
  if (!day) return null;
  return day.exercises.find(e => e.id === exerciseId);
};

export const getDayById = (dayId) => {
  return workoutDays.find(d => d.id === dayId);
};
